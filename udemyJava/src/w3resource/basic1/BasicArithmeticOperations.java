package w3resource.basic1;

public class BasicArithmeticOperations {
    public static void main(String[] args) {
        int numF = 125, numS = 24;

        System.out.println(numF + numS);
        System.out.println(numF - numS);
        System.out.println(numF * numS);
        System.out.println(numF / numS);
        System.out.println(numF % numS);
    }
}
